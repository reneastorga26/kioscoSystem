    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel model = (DefaultTableModel)tablaFabricante.getModel();
        
        fabricante.setIdFabricante(Long.valueOf(String.valueOf(
                model.getValueAt(tablaFabricante.getSelectedRow(),0))));
                
        model.removeRow(tablaFabricante.getSelectedRow());
        fabricante.eliminarBD(fabricante.getIdFabricante());
    }//GEN-LAST:event_jButton3ActionPerformed

