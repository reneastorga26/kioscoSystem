    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel model = (DefaultTableModel)tablaTipoProducto.getModel();
        
        tipoProducto.setIdTipoProducto(Long.valueOf(String.valueOf(
                model.getValueAt(tablaTipoProducto.getSelectedRow(),0))));
                
        model.removeRow(tablaTipoProducto.getSelectedRow());
        tipoProducto.eliminarBD(tipoProducto.getIdTipoProducto());
    }//GEN-LAST:event_jButton3ActionPerformed

