    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(alta){
            tipoConcepto = new TipoConcepto();
        }
        tipoConcepto.setDescripcion(txtDescripcion.getText());
        
        long idTipoConcepto = tipoConcepto.guardarBD();
        JOptionPane.showMessageDialog(
            null, "EL CONCEPTO SE HA GUARDADO CORRECTAMENTE","Mensaje",
            JOptionPane.INFORMATION_MESSAGE);
        
        Object [] fila = new Object [2];
        fila [0] = idTipoConcepto;
        fila [1] = tipoConcepto.getDescripcion();
        modeloTabla.addRow(fila);
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

